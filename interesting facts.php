<p> <b>First Generation (1G) </b><br>
<br>
•	 These telephones were the first and the foremost cell phones to be utilized, which was presented in 1982 and finished in mid-1990. It was utilized for voice benefits and depended on the technology called Advanced Mobile Phone System (AMPS). The AMPS framework was frequency modulated and utilized the frequency division multiple access (FDMA) with a channel limited to 30 KHz and with a frequency band of 824- 894 MHz<br>
<br>
•	The primary highlights of 1G are: Speed- 2.4 Kbps, Allows voice brings in one nation, Use analog signal, Poor voice quality, Poor battery life, large phone measure, Limited capacity, poor handoff unwavering quality, Poor security, and Offered low level of spectrum efficiency.<br>
<br>
•	It presents Mobile Technologies, for example, Mobile Telephone System (MTS), Advanced Mobile Telephone System (AMTS), Improved Mobile Telephone Service (IMTS) and Push to Talk (PTT). It has low capacity, problematic hand-off, poor voice quality and no security at all since voice gets back to be played in radio towers, making these calls helpless to undesirable in listening stealthily by outsiders.<br>
<br>
•	The main drawback of 1G technology is that it uses analog signals rather than digital signals, this is less effective means of transmitting information, it is slower, and the signals cannot reach as far in terms of secluded areas so 2G is coming to overcome these problems.<br>
<hr></p>
<p> <b>SECOND GENERATION (2G) </b><br>
<br>
•	2G refers to the second generation of mobile networks based on GSM. The radio signals used by the 1G network were analog, while 2G networks were digital. 2G capabilities were achieved by allowing multiple users on a single channel via multiplexing. During 2G, Cellular phones were used for data also along with voice. Some of the key features of 2G were:<br>
<br>
•	Data speeds of up to 64 kbps<br>
•	Use of digital signals instead of analog<br>
•	Enabled services such as SMS and MMS (Multimedia Message)<br>
•	Provided better quality voice calls<br>
•	It used a bandwidth of 30 to 200 KHz<br>
<hr>
</p>
<p><b>THIRD GENERATION (3G) </b><br>
<br>
•	The 3G standard utilizes Universal Mobile Telecommunications System (UMTS) as its core network architecture. 3G network combines aspects of the 2G network with new technologies and protocols to deliver a significantly faster data rate. By using packet switching, the original technology was improved to allow speeds up to 14 Mbps. It used Wide Band Wireless Network that increased clarity. It operates at a range of 2100MHz and has a bandwidth of 15-20MHz. Some of the main features of 3G are:<br>
<br>
•	Speed of up to 2 Mbps<br>
•	Increased bandwidth and data transfer rates<br>
•	Send/receive large email messages<br>
•	Large capacities and broadband capabilities<br>
 
International Mobile Telecommunications-2000 (IMT-2000) were the specifications by the International Telecommunication Union for the 3G network. Theoretically, 21.6 Mbps in the max speed of HSPA+.
<hr>
</p>
<p> <b>FOURTH GENERATION (4G) </b><br>
<br>
•	The main difference between 3G and 4G is the data rate. There is also a huge difference between 3G and 4G technology. The key technologies that have made 4G possible are MIMO (Multiple Input Multiple Output) and OFDM (Orthogonal Frequency Division Multiplexing). The most important 4G standards are WiMAX and LTE. While 4G LTE is a major improvement over 3G speeds, it is technically not 4G. What is the difference between 4G and LTE?<br>
<br>
•	Even after it was widely available, many networks were not up to the required speed of 4G. 4G LTE is a “fourth generation long term evolution”, capable of delivering a very fast and secure internet connection. Basically, 4G is the predetermined standard for mobile network connections. 4G LTE is the term given to the path which has to be followed to achieve those predefined standards. Some of the features of 4G LTE are:<br>
<br>
•	Support interactive multimedia, voice, video<br>
•	High speed, high capacity and low cost per bit (Speeds of up to 20 Mbps or more)<br>
•	Global and scalable mobile networks.<br>
•	Ad hoc and multi-hop networks.<br>
 <hr>
</p>
<p>  <b>FIFTH GENERATION (5G) </b> <br>
<br>
•	5G uses the rarely used radio millimeter bands in the 30 GHz to 300 GHz Testing of 5G range in mmWave has produced results approximately 500 meters from the tower. Using small cells, carriers using millimetre wave for the deployment of 5G can improve overall coverage area. Combined with Beamforming, small cells can deliver extremely fast coverage with low latency.<br>
<br>
•	Low latency is one of 5G’s most important features. 5G uses a scalable orthogonal frequency-division multiplexing (OFDM) framework. 5G benefits greatly from this and can have latency as low as one millisecond with realistic estimates to be around 1 – 10 seconds. 5G is estimated to be 60 to 120 times faster than the average 4G latency.<br>
<br>
 5G technology will be deployed by 2020. It provides the great feature to users, having higher data rate 1Gbps or higher. 5G support 4G+WWWW (4th Generation +Wireless World Wide Web). It operates on IPv6 protocol. Fifth generation technology utilizes CDMA and BDMA and millimeter wireless which approves speed is higher than 100Mbps at full speed and more prominent than 1Gbps at low speed.<hr>
