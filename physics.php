		  <div class="modal-body">

    <img class="img-responsive img-border img-full" src="img/wifi.jpg" alt="">
    
    <p>
    <hr>
    <b> Wired Equivalent Privacy (WEP)</b><br><br>
The first security protocol was named Wired Equivalent Privacy or WEP. It was the standard protocol from 1999 to 2004. While this version was made to protect, it had poor security and was hard to configure.<br><br>
Cryptographic technology imports were restricted at the time, meaning that more manufacturers could only use 64-bit encryption. This is a very low bit encryption compared to the 128-bit or 256-bit options available today. Ultimately, WEP was abandoned for a more advanced solution.
Systems that still use WEP aren’t secure. If you have a system with WEP, it should be upgraded or replaced. When connecting to WiFi, if an establishment has WEP, your Internet activity will not be secure.<hr>
<b>WiFi Protected Access (WPA)</b><br><br>
To improve the functions of WEP, WiFi Protected Access or WPA was created in 2003. This temporary enhancement still has relatively poor security but is easier to configure. WPA uses Temporal Key Integrity Protocol (TKIP) for more secure encryption than WEP offered.<br><br>
As the WiFi Alliance made this transition to a more advanced protocol, they had to keep some of the same elements of WEP so older devices would still be compatible. Unfortunately, this means vulnerabilities, such as the WiFi Protected Setup feature which can be hacked relatively easily, are still present in the updated version of WPA.<hr>
<b>WiFi Protected Access 2 (WPA2)</b><br><br>
A year later, in 2004, WiFi Protected Access 2 became available. WPA2 has stronger security and is easier to configure than the prior options. The main difference with WPA2 is that it uses the Advanced Encryption Standard (AES) instead of TKIP. AES is able to secure top-secret government information, so it’s a good option for keeping a personal device or company WiFi safe.<br><br>
The only notable vulnerability of WPA2 is that once someone has access to the network, they can attack other devices connected to the network. This is an issue if a company has an internal threat, such as an unhappy employee, who hacks into the other devices on the company’s network.<hr>
<b>WiFi Protected Access 3 (WPA3)</b><br><br>
As vulnerabilities are recognized, advancements are made. In 2018, the WiFi Alliance introduced WPA3. This new version will have, “new features to simplify WiFi security, enable more robust authentication and deliver increased cryptographic strength for highly sensitive data markets.” WPA3 is still being implemented so WPA3-certified hardware isn’t an available option for most people.
</p>
