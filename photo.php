<h3>Annie Leibovitz</h3>
<img class="img-responsive img-border img-full" src="img/vani.jpg" alt="">
<p>Annie Leibovitz is a portrait photographer who in 1970 landed a job at Rolling Stone and went on to create a distinctive look for the publication as chief photographer. In 1983, she began working for the entertainment magazine Vanity Fair, continuing to produce images that would be deemed iconic and provocative. Having also worked on high-profile advertising campaigns, Leibovitz's images have been showcased in several books and major exhibitions around the world. </p>
<p>To date, a number of Vanity Fair covers have featured Leibovitz’s stunning—and often controversial—portraits of celebrities. Demi Moore (very pregnant and very nude followed by a body painting shoot), Whoopi Goldberg (half-submerged in a bathtub of milk), Sylvester Stallone (appearing nude in a pose inspired by Rodin's "The Thinker") and Caitlyn Jenner (in a corset after having publicly revealed her identity as a woman) are among the most remembered celebs to grace the cover. Known for her ability to make her sitters become physically involved in her work, another of Leibovitz’s most famous portraits is of the late artist Keith Haring, who painted himself like a canvas for the photo.</p>
<p>She uses a very distinct lighting setup which delivers a muted almost painterly image. Her photographs are very recognizable if you have studies her work even for a short period of time. Consistency in your photographic style is a great skill to attain because it means people can easily recognize your work and you will get hired more.</p>
<p>She has got the prestigious awards like " Glamour Award For The Visionary " and " Honorary Clio Award "</p>
<p>Her very known works was published as a book by her as " A Photographer's Life: 1990-2005 " where it was all about her works as depicted as photos</p>
<p><a href="https://www.youtube.com/watch?v=1cG_w896Z2g">click here to view her works in that book explained by a photographer</a></p>
<h3>Gregory Crewdson</h3>
<img class="img-responsive img-border img-full" src="img/vgeo.jpg" alt="">
<p>Gregory Crewdson (American, b.1962) is best known for his photographs, which feature elaborately staged, surreal tableaus of suburban life. Born in the Park Slope neighborhood of Brooklyn, NY, Crewdson played in a punk rock band as a teenager, before attending the State University of New York at Purchase, where he earned his BFA. After completing his MFA at Yale University in 1988, Crewdson began working on the first of his many lengthy photographic series, Natural Wonder, depicting highly constructed scenes showing the grotesque narratives of paranoia, lust, and fantasy that are played out in suburbia.</p>
<p>Crewdson’s subsequent series, Hover (1995) and Twilight (2003), document dramatic and strange occurrences within typical middle-class neighborhoods, creating bizarre and unsettling images within fairly ordinary settings. Since 1993, Crewdson has served as a professor at Yale University, where he continues to teach and work today. Crewdson’s photographs have proven to be successful both in and outside of gallery settings; in addition to numerous shows at White Cube gallery in London, his work has been used in HBO advertisements and trailers for the popular television series, Six Feet Under (2001–2005).</p>          
<p>He spends thousands and even millions of dollars to create a single photograph. Imagine the set of a major motion picture being used for one single image. Gregory began experimenting with these large-scale sets in college and just keep making them more extravagant.</p>
<p><a href="https://www.youtube.com/watch?v=BpIRm5BsXeE">click here to view a video where he himself explains about his art</a></p>
<h3>Richard Avedon</h3>
<img class="img-responsive img-border img-full" src="img/vric.jpg" alt="">
<p>American photographer Richard Avedon was best known for his work in the fashion world and for his minimalist portraits. He worked first as a photographer for the Merchant Marines, taking identification photos. He then moved to fashion, shooting for Harper's Bazaar and Vogue, demanding that his models convey emotion and movement, a departure from the norm of motionless fashion photography.</p>
<p>While some regarded this work as a fashion and portrait photographer as poking fun at blue collar workers, I think that it sheds a beautiful light on the professions these people had. It offered viewers in galleries and insight into everyday life and work that they were not accustomed to and put it right in front of their faces to experience. These images are also stunningly beautiful works of art.</p>
<p><a href="https://www.youtube.com/watch?v=Ltxph09O3Qk">click here to view his works explianed by a photographer</a></p>
<h3>Elliott Erwitt</h3>
<img class="img-responsive img-border img-full" src="img/vell.jpg" alt="">
<p>Moving away from large format photography, Elliott Erwitt primarily shot on a 35mm camera. Erwitt was originally from France and immigrated to the United States to go to college. He worked as a photography assistant in the US army where he honed his skills.</p>
<p>Elliott focused a lot of his attention on street photography and is one of the wittiest street photographers ever to grace the earth. His images are both deep and funny at the same time images. If you like street photography, Elliott Erwitt will blow you away!</p>
<p><a href="https://www.youtube.com/watch?v=4djabjkQzkE&t=141s">click here to join a voyage of photography to Scotland</a></p>
<h3>Vivian Maier</h3>
<img class="img-responsive img-border img-full" src="img/vvm.jpg" alt="">
<p>Probably known as one of the most interesting secret photographers of all time, Vivian Maier did not become famous until after her death. In 2007 John Maloof purchased a trunk of negatives and undeveloped film from an auction. Little did he know that the contents of this trunk would include thousands of images by a soon to be a well-known photographer named Vivian Maier.</p>
<p>What we now know about Vivian is that she spent her life as a nanny. She mainly photographed on a twin lens 6×6 camera and brought it everywhere she went. The majority of her photographs were taken in Chicago and New York. She simply photographed everything that interested her when she walked down the streets.</p>
<p>Although she was known to be reclusive and weird, her photographs do not depict this side of her. Vivian’s images are bold showing strangers staring right into her camera. I can only imagine what people thought when they noticed a nanny pointing a camera at them on the streets of Chicago.</p>
<p>If you want to learn more about Vivian Maier I recommend watching a wonderful documentary made about her entitled Finding Vivian Maier.</p>
