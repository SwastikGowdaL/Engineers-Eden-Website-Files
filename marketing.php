<h3>What is Marketing</h3>
<p>In simplest terms, marketing is the act of driving profitable customer action. It spans the full scope of strategies and tactics organizations use to position products and services in the marketplace, and motivate target audiences to make a purchase.</p>
<h3>Key things about marketing</h3>
<p>For all its complexity, at its core, marketing revolves around four things ,Also called as 4p's: product, price, promotion, and place. Tactics and channels change, but these are the concepts everything else revolves around, and they’re principles that never change.</p>
<p><b>Product:</b>A simple definition of a product (in marketing terms) is “anything that meets a consumer and provides value that can be obtained by a customer through a marketing exchange.<br>
The three things to be known about the product in the view of marketing is:<br>
1>Presenting the product mainly dealing with physical appearence.<br>
2>Finding the difference from other competative products<br>
3>How well it is serviced like about warranties, handling returns..etc<br></p>
<p><b>Price:</b>If marketing is all about driving profitable action, then prices need to be set at a level the market will support.<br>
Things about price to be in notice:<br>
1>Marketing rate per unit of the product:some market analysis and competitive researches determine what’s a fair price for a product, given its cost to produce, and what people are willing to pay.<br>
2>Discounts and its time of release<br>
3>The option of Payment to be easier<br></p>
<p><b>Place:</b>The right product needs to be in the right place for people to find it and buy it.<br>
1>The place where the product gets it value as it is a requiremnt to that locality.<br>
2>People in that place must have a knowledge about it.<br>   
3>Even if it is sold online/offline matters.<br>
<p><b>Promotion:</b>One of the main streams where most of the products fail to reach the right customers it needs<br>
So to be cautious in this we have to look out for this:<br>
1>Channels you use to promote the product means like offline or online.<br>
2>Reading people requiremnets on that basis designing to promote.<br>
3>It is also required to know the marketing psychology towards the people.<br>
4>To know what message to be conveyed and how effective to be conveyed.<br>
<h3>Types Of Marketing</h3>
<p><b>1. Paid advertising</b>:
This includes multiple approaches for marketing. It includes traditional approaches like TVCs and print media advertising. Also, one of the most well-known marketing approach is internet marketing. It includes various methods like PPC (Pay per click) and paid advertising.
<br>
<b>2. Cause marketing</b>:
Cause marketing links the services and products of a company to a social cause or issue. It is also well known as cause related marketing.
<br>
<b>3. Relationship marketing</b>:
This type of marketing is basically focused on customer building. Enhancing existing relationships with customers and improving customer loyalty.
<br>
<b>4. Undercover marketing</b>:
This type of marketing strategy focuses on marketing the product while customers remain unaware of the marketing strategy. It is also known as stealth marketing.
<br>
<b>5. Word of mouth</b>:
It totally relies on what impression you leave on people. It is traditionally the most important type of marketing strategy. Being heard is important in business world. When you give quality services to customers, it is likely that they’d promote you.
<br>
<b>6. Internet marketing</b>:
It is also known as cloud marketing. It usually happens over the internet. All the marketing items are shared on the internet and promoted on various platforms via multiple approaches.
<br>
<b>7. Transactional marketing</b>:
Sales is particularly the most challenging work. Even for the largest retailers, selling is always tough especially when there are high volume targets. However with the new marketing strategies, selling isn’t as difficult as it was. In transactional marketing the retailers encourage customers to buy with shopping coupons, discounts and huge events. It enhances the chances of sales and motivates the target audience to buy the promoted products.
<br>
<b>8. Diversity marketing</b>:
It caters diverse audience by customizing and integrating different marketing strategies. It covers different aspects like cultural, beliefs, attitudes, views and other specific needs.
<br>
