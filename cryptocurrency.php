<i><p>A cryptocurrency (or crypto currency) is a digital asset designed to work as a medium of exchange wherein individual coin ownership records are stored in a digital ledger or computerized database using strong cryptocurrency to secure transaction record entries, to control the creation of additional digital coin records, and to verify the transfer of coin ownership.</p>	
<h2>How do cryptocurrencies work? </h2>
<p>Cryptocurrencies use decentralised technology to let users make secure payments and store money without the need to use their name or go through a bank. They run on a distributed public ledger called blockchain, which is a record of all transactions updated and held by currency holders.
</p>
<h3>Why would you use a cryptocurrency</h3>
<p>Cryptocurrencies are known for being secure and providing a level of anonymity. Transactions in them cannot be faked or reversed and there tend to be low fees, making it more reliable than conventional currency. Their decentralised nature means they are available to everyone, although they can be complicated to set up and few stores accept them for spending.
As a new form of cash, the cryptocurrency markets have been known to boom suddenly, meaning a small investment can become a large sum overnight. 
This has led to a spur in professional and amateur speculators investing in Bitcoin, Ethereum and other cryptocurrencies, seeing them either as a quick way to make returns or as part of an investment portfolio.
</p>
<h3>Most common cryptocurrencies</h3>
<p><b>Bitcoin:</b> Bitcoin was the first and is the most commonly traded cryptocurrency to date.  The currency was developed by Satoshi Nakamoto in 2009, a mysterious figure who developed its blockchain. It has a market capitalisation of around $128 billion as of May 2018. <br>
<b>Ethereum: </b>Developed in 2015, Ether is the currency token used in Ethereum blockchain, the second most popular and valuable cryptocurrency. Ether has a market capitalisation of around $56 billion as of May 2018. However, ether has had a turbulent journey. After a major hack in 2016 it split into two currencies, while its value at one stage it reached as high as $1,300 but it has previously crashed briefly to as low as 10 cents. It has proved hugely popular as a launch pad for other cryptocurrencies in 2017, which use the Ethereum blockchain's code.<br>
<b>Ripple: </b>(2012) Ripple can be used to track more kinds of transactions, not just of the cryptocurrency. The company behind it has worked with banks and financial institutions, including Santander. It has a market capitalisation of around $24 billion.<br>
<b>Litecoin: </b>This currency is most similar in form to bitcoin, but has moved more quickly to develop new innovations, including faster payments and processes to allow many more transactions. The total value of all Litecoin is around $6 billion.</p>
<h3>What Is a Smart Contract?</h3>
<p>A smart contract is a self-executing contract with the terms of the agreement between buyer and seller being directly written into lines of code. The code and the agreements contained therein exist across a distributed, decentralized blockchain network. The code controls the execution, and transactions are trackable and irreversible.
Smart contracts permit trusted transactions and agreements to be carried out among disparate, anonymous parties without the need for a central authority, legal system, or external enforcement mechanism.
While blockchain technology has come to be thought of primarily as the foundation for bitcoin, it has evolved far beyond underpinning the virtual currency.</p>
