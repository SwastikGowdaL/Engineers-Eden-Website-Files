          <p><b>Let’s Talk A Little About Cryptocurrency</b><br>Cryptocurrencies, like bitcoin, are currencies that exist solely in digital. There are no physical golden coins with a big <em>B</em> on them. Moreover, owning these non-real coins entails a new idea of <em>ownership</em>. You don’t literally have it in your hands, or even in your bank account, but you have the ability to transfer <em>ownership</em> to someone else simply by creating a record in the blockchain. Rather than using bills, your transfer is pure data. The Bitcoin protocol is built on the blockchain.<br>
<b>What is Blockchain?</b><br><em>The Blockchain</em> is a public ledger(like  the register a teacher has while taking attendance) where transactions are recorded and confirmed anonymously. It’s a record of events that is shared between many parties. More importantly, once information is entered, it cannot be altered. So, if the blockchain is the public record, what is being recorded? What are all of these <em>transactions</em>?<br>At its most basic level, blockchain is literally just a chain of blocks, but not in the traditional sense of those words. When we say the words <em>block</em> and <em>chain</em> in this context, we are actually talking about digital information (the “block”) stored in a public database (the “chain”).<br><em>Blocks</em> on the blockchain are made up of digital pieces of information. Specifically, they have three parts:</p><br><ul><em><li>Blocks store information about transactions like the date, time, and dollar amount of your most recent purchase from Amazon.</li><li>Blocks store information about who is participating in transactions. A block for your splurge purchase from Amazon would record your name along with Amazon.com, Inc. (AMZN). Instead of using your actual name, your purchase is recorded without any identifying information using a unique <em>digital signature</em>, sort of like a username.</li><li>Blocks store information that distinguishes them from other blocks. Much like you and I have names to distinguish us from one another, each block stores a unique code called a “hash” that allows us to tell it apart from every other block. Hashes are cryptographic codes created by special algorithms. Let’s say you made your splurge purchase on Amazon, but while it’s in transit, you decide you just can’t resist and need a second one. Even though the details of your new transaction would look nearly identical to your earlier purchase, we can still tell the blocks apart because of their unique codes.</li></em></ul><br><img class="img-responsive img-border img-full" src="img/Blockchain.jpg" alt=""><br><p>While the block in the example above is being used to store a single purchase from Amazon, the reality is a little different. A single block on the Bitcoin blockchain can actually store up to 1 MB of data. Depending on the size of the transactions, that means a single block can house a few thousand transactions under one roof.<br>
<b>Where exactly is this chain located?</b><br>
Due to the open nature of cryptocurrencies, and the importance of the public having access to other blocks, the blockchain isn’t located on just one guy’s large computer. For example, the bitcoin blockchain is actually managed by distributed nodes. These nodes all have a copy of the entire blockchain. Nodes will forever come and go, synchronizing their own copies of the chain with those of other users. By distributing copies and access, the chain can’t simply “go down,” or disappear. It’s a decentralized system that is both sturdy and secure.<br>
<b>Application IRL</b><br>
You have all these people, all over the world, who have bitcoin. There are likely many millions of people around the world who own at least a portion of a bitcoin. Let’s say one of those millions of people wants to spend their bitcoin on groceries. This is where the blockchain comes in.<br>When it comes to printed money, the use of printed currency is regulated and verified by a central authority, usually a bank or government—but Bitcoin is not controlled by anyone. Instead, transactions made in bitcoin are verified by a network of computers. This is what is meant by the Bitcoin network and blockchain being <em>decentralized</em>.<br>
<b>Mining Cryptocurrency</b><br>When one person pays another for goods using bitcoin, computers on the Bitcoin network race to verify the transaction. In order to do so, users run a program on their computers and try to solve a complex mathematical problem, called a <em>hash</em>. When a computer solves the problem by <em>hashing</em> a block, its algorithmic work will have also verified the block’s transactions. As we described above, the completed transaction is publicly recorded and stored as a block on the blockchain, at which point it becomes unalterable. In the case of Bitcoin, and most other blockchains, computers that successfully verify blocks are rewarded for their labour with cryptocurrency. This is commonly referred to as <em>mining</em>.<br>
<b>History</b><br>The goal of blockchain is to allow digital information to be recorded and distributed, but not edited. That concept can be difficult to wrap our heads around without seeing the technology in action, so let’s take a look at how the earliest application of blockchain technology actually worked.<br>Blockchain technology was first outlined in 1991 by Stuart Haber and W. Scott Stornetta, two researchers who wanted to implement a system where document timestamps could not be tampered with. In 1992, Haber, Stornetta, and Dave Bayer incorporated hash trees to the design, which improved its efficiency by allowing several document certificates to be collected into one block. But it wasn’t until almost two decades later, with the launch of Bitcoin in January 2009, that blockchain had its first real-world application.<br>In a research paper introducing the digital currency, Bitcoin’s pseudonymous creator Satoshi Nakamoto referred to it as “a new electronic cash system that’s fully peer-to-peer, with no trusted third party.”<br>With many practical applications for the technology already being implemented and explored, blockchain is finally making a name for itself at age twenty-seven, in no small part because of bitcoin and cryptocurrency. As a buzzword on the tongue of every investor in the nation, blockchain stands to make business and government operations more accurate, efficient, and secure.<br></p>
