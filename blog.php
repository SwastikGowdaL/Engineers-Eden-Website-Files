			<h4 class="modal-title">Difference between Hackers</h4>
		  </div>
		  <div class="modal-body">
			The best way to differentiate between black hat and white hat hackers, is by taking a
look at their motives. Black Hat hackers are motivated by malicious intent,
manifested by personal gains, profit, or harassment; whereas White Hat hackers
seek out and remedy vulnerabilities, so as to prevent Black Hats from taking
advantage.
<p style='color: red'>The other ways to draw a distinction between White Hat and Black Hat hackers
include:</p>
<p>• Techniques used: White Hat hackers duplicate the techniques and methods
followed by malicious hackers in order to find out the system discrepancies,
replicating all the latter’s steps to find out how a system attack occurred or
may occur. If they find a weak point in the system or network, they report it
immediately and fix the flaw.</p>
<p>• Legality: Even though White Hat hacking follows the same techniques and
methods as Black Hat hacking, only one is legally acceptable. Black Hat
hackers break the law by penetrating systems without consent.</p>
<p>• Ownership: White Hat hackers are employed by organizations to penetrate
their systems and detect security issues. Black hat hackers neither own the
system nor work for someone who owns it.</p>
