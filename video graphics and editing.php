<h2>Best Free Video editing apps</h2>
<h3>Quik</h3>
<p>Quik analyses your videos to detect colours and faces in order to frame your footage perfectly. This free app lets you add transitions and effects onto a maximum of 200 photos and video clips from your photo library or GoPro Plus. Not only can you trim, zoom and rotate photos and video clips, but you can also choose from 26 different themes and a range of fonts, filters and graphics. You can create HD 1080p or 720p videos in cinema, square or portrait formatting for social sharing and even save your draft projects for up to seven days.<br>
<b>Features:</b> Packed with features, access to 100+ free songs, Flashbacks and QuikStories create videos based on your latest footage, syncs footage to the beat of your soundtrack, AI editing assistance, and it's for free<br>
<b>drawbacks:</b> Minimal control and no trimming option, users reported that it can be glitchy at times<br>
Available for: iPhone, iPad and Android.</p>
<p><a href="https://play.google.com/store/apps/details?id=com.wynntech.myquikvid&hl=en_IN">click here to download quik in play store</p></a>
<h3>Adobe Premiere Rush</h3>
<p>Premiere Rush has plenty of neat features. Create a video quickly, either with an automatic editing function or more advanced customised editing features that let you do everything from trimming clips to ducking the music volume over dialogue. You can sync your video to the beat of your chosen soundtrack and add all the usual finishing touches, like titles and transitions. Premier Clip integrates seamlessly with other Adobe products – such as Premier Pro, Lightroom and Capture CC – while their CreativeSync cloud software automatically updates your project across all your devices.<br>
Features: Good suite of basic features, Adobe integration and support, and it's free<br>
Drawbacks: Lacks vertical editing, requires Premier Pro for anything other than basic videos.<br>
Available for: iPhone, iPad and Android.</p>
<p><a href="https://play.google.com/store/apps/details?id=com.adobe.premiererush.videoeditor&hl=en_IN">click here to download Adobe Premiere Rush in play store</p></a>
<h3>We Video</h3>
<p>WeVideo put full control in your hands over all the creative elements, like text, transmissions and motion effects. You can even let your imagination run wild with basic green screen special effects. Cloud storage lets you start your project on one device and pick up where you left off on another. Plus, when you buy into one of their plans, you gain access to a vast library of licensed and royalty-free video clips, images and music tracks to add some extra gloss to your video.<br>
Features: Easy interface, multi-device cloud storage,Free, with in-app purchases (Upgrade plans start from $4.99 per month).<br>
Drawbacks: Free version includes a watermark and has limited functionality.<br>
Available for: iPhone, iPad, Android and Windows</p>
<p><a href="https://play.google.com/store/apps/details?id=com.wevideo.mobile.android&hl=en_IN">click here to download WeVideo in play store</p></a>
<h3>Kine Master</h3>
<p>KineMaster say their video editing app is made for professionals but is easy enough for everyone to use. It’s true that this app certainly veers towards the more serious side of editing. This is evidenced by frame-by-frame granular editing, pre-render instant previews and support for up to four audio tracks and unlimited audio clips. KineMaster also enables up to two video layers, which can be animated using preset effects or keyframe animation, chroma key compositing, precise colour adjustment and speed control from 0.25x up to 1.5x without any audio pitch distortion.<br>
Features: Packed with features, supports multiple layers of video and multi-track audio,Free (Pro Edition $4.99 monthly or $39.99 annually).<br>
Drawbacks: Watermarks on limited free version, users reported issues with chroma key compositing and exporting videos, requires subscription for full suite of features.<br>
Available for: iPhone, iPad and Android.</p>
<p><a href="https://play.google.com/store/apps/details?id=com.nexstreaming.app.kinemasterfree&hl=en_IN">click here to download KineMaster in play store</p></a>
<h3>PowerDirector</h3>
<p> PowerDirector offers powerful multiple track timeline video editing. You gain a host of video effects, FX editing with intuitive drag and drop controls, chroma key editable green and blue screen backgrounds, slow motion editing (even if you didn’t film your video in slow motion originally), vertical videos specifically for mobile, and all the usual transitions and cropping tools. When it’s all ready, you can export your video in 720p or, if you’re willing to buy an in-app upgrade, Full HD 1080p and 4K.<br>
Features: Vertical video editing, easy to use, good range of features and it's free.<br>
Drawbacks: Only on Android and Windows, 1080p and 4K require in-app purchase, watermark on videos.<br>
Available for: Android and Windows phones and tablets.</p>
