          What is 3D printing? <br>
<br>
3D printing is the process of creating a physical object from a digital model. 3D printing is an additive process. Layers of plastic are built up one after the other to create an object.<br>
<br>
How does 3D printing impact the economy?<br> 
3D printing is a component of the maker movement, which has benefits to communities, education, entrepreneurship, and traditional enterprises. It helps foster the creation of new products and new companies, and teaches skills transferable into a wide variety of technical and professional jobs.<br>
<br>
How expensive is 3D printing compared to traditional manufacturing processes? <br>
That depends. It's far less expensive and takes far less time to create prototypes, jigs, tools, and fixtures using 3D printing. But once the setup and tooling costs are paid for, traditional manufacturing techniques like injection molding can produce objects in volume more quickly and at lower cost.<br>
<br>
How does 3D printing affect the supply chain? <br>
3D printing is ideal for short-run manufacturing and small production jobs. It also allows spare parts to be "stored in the cloud," so physical inventory isn't required until there is a demand for an object. By delivering 3D objects across the globe in digital form and printing locally, the cost and time of shipping can be completely eliminated.<br>
<br>
Can 3D printing transform the manufacturing industry?<br> 
The manufacturing industry is undergoing a vast transformation of which 3D printing is one element. Other factors include a huge increase in data volume and processing, improved analytics, improved human factors, and the automation of various production processes.<br>
<br>
What is 3D printing?<br>
<br>
3D printing is the process of creating objects (usually plastic, but sometimes metal or composite material) from a digital model. Most 3D printers add material to the object one very thin layer at a time, which is why 3D printers are classified as "additive manufacturing."<br>
<br>
HOW DOES ADDITIVE MANUFACTURING WORK?<br>
<br>
The analogy to printing is not perfect. Computer printers generally operate a row at a time. 3D printers work much more like plotters, moving a print head along both the X and Y axis to draw a pattern. In the case of a 3D printer, the pattern is usually drawn with plastic, not ink. What makes the 3D printer three-dimensional is that once a pattern is drawn, the print head moves up (or the print surface moves down), and another pattern is drawn on top of the first.<br>
<br>
HOW DOES A 3D PRINTER WORK?<br>
<br>
<br>
There are several  types of 3D printers out there, but we'll be focusing on two: fused-deposition modeling (or FDM) and stereolithography (or SLA).<br>
<br>

FDM starts with rolls of filament as its source material. These typically come in strands either 1.75 or 2.85 millimeters thick, rolled onto a spindle. An FDM printer heats the filament, squeezes it out through an extruder nozzle, and lays down layers on a build surface. The layers are incredibly thin, and as each molten layer is laid down on top of the previous layer, it partially fuses as it cools.<br>
<br>

Also: 3D printing and robots power largest furniture maker in the world <br>

<br>
Over time -- sometimes a lot of time -- an object is built up from hundreds or thousands of these layers.<br>

<br>SLA starts with a liquid resin as its source material. A build tray is lowered into the resin (usually upside down) and light (sometimes from an LCD, sometimes from an ultraviolet laser) creates a chemical reaction in the resin that causes it to harden. As each layer is exposed to the light, the printer raises the build platform slightly out of the resin pool, exposing the next layer to light.<br>

