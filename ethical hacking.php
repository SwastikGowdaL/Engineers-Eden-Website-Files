		  <div class="modal-body">
			<p style='color: red'>Roles and Responsibilities of an Ethical Hacker</p>
Ethical Hackers must follow certain guidelines in order to perform hacking legally. A
good hacker knows his or her responsibility and adheres to all of the ethical
guidelines. Here are the most important rules of Ethical Hacking:
<p>• An ethical hacker must seek authorization from the organization that owns the
system. Hackers should obtain complete approval before performing any
security assessment on the system or network.</p>
<p>• Determine the scope of their assessment and make known their plan to the
organization.</p>
<p>• Report any security breaches and vulnerabilities found in the system or
network.</p>
<p>• Keep their discoveries confidential. As their purpose is to secure the system
or network, ethical hackers should agree to and respect their non-disclosure
agreement.</p>
<p>• Erase all traces of the hack after checking the system for any vulnerability. It
prevents malicious hackers from entering the system through the identified
loopholes.</p>
