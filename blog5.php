</p>
<p><b>THIRD GENERATION (3G) </b><br>
<br>
•	The 3G standard utilizes Universal Mobile Telecommunications System (UMTS) as its core network architecture. 3G network combines aspects of the 2G network with new technologies and protocols to deliver a significantly faster data rate. By using packet switching, the original technology was improved to allow speeds up to 14 Mbps. It used Wide Band Wireless Network that increased clarity. It operates at a range of 2100MHz and has a bandwidth of 15-20MHz. Some of the main features of 3G are:<br>
<br>
•	Speed of up to 2 Mbps<br>
•	Increased bandwidth and data transfer rates<br>
•	Send/receive large email messages<br>
•	Large capacities and broadband capabilities<br>
 
