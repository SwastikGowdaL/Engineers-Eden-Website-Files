<p> <b>SECOND GENERATION (2G) </b><br>
<br>
•	2G refers to the second generation of mobile networks based on GSM. The radio signals used by the 1G network were analog, while 2G networks were digital. 2G capabilities were achieved by allowing multiple users on a single channel via multiplexing. During 2G, Cellular phones were used for data also along with voice. Some of the key features of 2G were:<br>
<br>
•	Data speeds of up to 64 kbps<br>
•	Use of digital signals instead of analog<br>
•	Enabled services such as SMS and MMS (Multimedia Message)<br>
•	Provided better quality voice calls<br>
•	It used a bandwidth of 30 to 200 KHz<br>
