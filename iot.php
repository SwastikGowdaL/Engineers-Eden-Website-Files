<p style='color: indigo'><i>" When something is connected to the internet, that means that it can send information or receive information, or both. This ability to send and/or receive information makes things smart.... Here where Internet Of Things exist "</p>
<h3>What is IOT</h3>
<p>The Internet of Things (IoT) describes the network of physical objects—“things”—that are embedded with sensors, software, and other technologies for the purpose of connecting and exchanging data with other devices and systems over the internet. </p>
<p>Although the name “Internet of Things” may be unfamiliar, you may recognize this connected ecosystem better as “smart homes” or “connected homes,” which include the various IoT devices that make your home life easier. But IoT devices are also found outside the home. They can range from a Wi-Fi pet camera on your bookshelf to a medical device implanted in your body, like a pacemaker. As long as the device is able to connect to the internet and has sensors that transmit data, it can be considered an IoT device. Although your smartphone can do both, but it’s not an IoT device.
<h3>How Does It Work</h3>
<p>A complete IoT system integrates four distinct components: sensors/devices, connectivity, data processing, and a user interface. Below it is briefly explained :<br>

<b>1) Sensors/Devices</b>:
First, sensors or devices collect data from their environment. This could be as simple as a temperature reading or as complex as a full video feed.
I use “sensors/devices,” because multiple sensors can be bundled together or sensors can be part of a device that does more than just sense things. For example, your phone is a device that has multiple sensors (camera, accelerometer, GPS, etc), but your phone is not just a sensor.
However, whether it’s a standalone sensor or a full device, in this first step data is being collected from the environment by something.<br>
<b>2) Connectivity</b>:
Next, that data is sent to the cloud (what’s the cloud?), but it needs a way to get there!
The sensors/devices can be connected to the cloud through a variety of methods including: cellular, satellite, WiFi, Bluetooth, low-power wide-area networks (LPWAN), or connecting directly to the internet via ethernet.
Each option has tradeoffs between power consumption, range and bandwidth (here’s a simple explanation). Choosing which connectivity option is best comes down to the specific IoT application, but they all accomplish the same task: getting data to the cloud.<br>
<b>3) Data Processing</b>:
Once the data gets to the cloud, software performs some kind of processing on it.
This could be very simple, such as checking that the temperature reading is within an acceptable range. Or it could also be very complex, such as using computer vision on video to identify objects (such as intruders in your house).
But what happens when the temperature is too high or if there is an intruder in your house? That’s where the user comes in.<br>
<b>4) User Interface</b>:
Next, the information is made useful to the end-user in some way. This could be via an alert to the user (email, text, notification, etc). For example, a text alert when the temperature is too high in the company’s cold storage.
Also, a user might have an interface that allows them to proactively check in on the system. For example, a user might want to check the video feeds in their house via a phone app or a web browser.
However, it’s not always a one-way street. Depending on the IoT application, the user may also be able to perform an action and affect the system. For example, the user might remotely adjust the temperature in the cold storage via an app on their phone.
And some actions are performed automatically. Rather than waiting for you to adjust the temperature, the system could do it automatically via predefined rules. And rather than just call you to alert you of an intruder, the IoT system could also automatically notify relevant authorities.</p>
<h3>Benefits of IOT</h3>
<p>The Internet of Things is designed to make our lives more convenient. Here are a few examples:<br>
>Smart bathroom scales working in tandem with your treadmill, delivering food preparation ideas to your laptop or smartphone, so you stay healthy.<br>
>Security devices monitoring your home, turning lights on and off as you enter and exit rooms, and streaming video so you can check in while you’re away.<br>
>Smart voice assistants placing your usual takeout order on command, making it a breeze to get fresh food delivered to your door.</p>
