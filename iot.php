         <p>  <img class="img-responsive img-border img-full" src="img/ard1.png" alt=""></p>

          <p>The Arduino Uno is an open-source microcontroller board based on the Microchip ATmega328P microcontroller and developed by Arduino.cc. The board is equipped with sets of digital and analog input/output (I/O) pins that may be interfaced to various expansion boards (shields) and other circuits. The board has 14 digital I/O pins (six capable of PWM output), 6 analog I/O pins, and is programmable with the Arduino IDE (Integrated Development Environment), via a type B USB cable. It can be powered by the USB cable or by an external 9-volt battery, though it accepts voltages between 7 and 20 volts. It is similar to the Arduino Nano and Leonardo.The hardware reference design is distributed under a Creative Commons Attribution Share-Alike 2.5 license and is available on the Arduino website. Layout and production files for some versions of the hardware are also available.<br>
<br>
The word "uno" means "one" in Italian and was chosen to mark the initial release of Arduino Software. The Uno board is the first in a series of USB-based Arduino boards; it and version 1.0 of the Arduino IDE were the reference versions of Arduino, which have now evolved to newer releases. The ATmega328 on the board comes preprogrammed with a bootloader that allows uploading new code to it without the use of an external hardware programmer.<br>
<br>
While the Uno communicates using the original STK500 protocol,it differs from all preceding boards in that it does not use the FTDI USB-to-serial driver chip. Instead, it uses the Atmega16U2 (Atmega8U2 up to version R2) programmed as a USB-to-serial converter.<br>
</p>
<p>  Technical specifications<br>
<br>
Microcontroller: Microchip ATmega328P <br>
Operating Voltage: 5 Volts <br>
Input Voltage: 7 to 20 Volts <br>
Digital I/O Pins: 14 (of which 6 can provide PWM output) <br>
UART: 1 <br>
I2C: 1 <br>
SPPI: 1 <br>
Analog Input Pins: 6 <br>
DC Current per I/O Pin: 20 mA <br>
DC Current for 3.3V Pin: 50 mA <br>
Flash Memory: 32 KB of which 0.5 KB used by bootloader <br>
SRAM: 2 KB <br>
EEPROM: 1 KB <br>
Clock Speed: 16 MHz <br>
Length: 68.6 mm <br>
Width: 53.4 mm <br>
Weight: 25 g <br>


