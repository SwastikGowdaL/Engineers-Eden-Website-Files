	<p> 
   <b> 1) Hydroxychloroquine (HCQ):</b><br>
   <img class="img-responsive img-border img-full" src="img/hcqs.jpg" alt=""> <br> It was first developed in India for the treatment of malaria. Two Indian firms, Ipca Laboratories and Zydus Cadila Ltd, are the world’s largest manufacturers of the drug. The Indian Council of Medical Research has also been using the drug to prevent infection among healthcare workers based on theoretical evidence that hydroxychloroquine does not allow the novel coronavirus, or SARS-COV2, to attach to cells. HCQ was touted as a potential coronavirus treatment by US President Donald Trump and some other global political leaders.    </p>
