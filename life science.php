    <p><b> 2) Covifor:</b><br><img class="img-responsive img-border img-full" src="img/cov.jpg" alt=""><br>
     The Drug Controller General of India (DCGI) has given approval to Hetero for manufacturing ‘Remdesivir’ for the treatment of Covid-19. Hetero’s generic version of Remdesivir will be marketed under the brand name ‘Covifor’ in India. It has been granted approval by DCGI for the treatment of suspected or laboratory-confirmed cases of Covid-19 in adults and children, hospitalized with severe symptoms of the disease. Covifor (Remdesivir) will be available in 100 mg vial (injectable) which has to be administered intravenously in a hospital setting under the supervision of a healthcare practitioner.</p><hr>
    <p><b> 3) Fabiflu:</b><br><img class="img-responsive img-border img-full" src="img/fab.jpg" alt=""><br> Glenmark Pharmaceuticals has launched antiviral drug Favipiravir, under the brand name FabiFlu, for the treatment of mild to moderate Covid patients. Priced at ₹103 per tablet, the prescription-based drug will be available as a 200 mg tablet at an MRP of ₹3,500 for a strip of 34 tablets.</p><hr>
    <p><b>4) Cipremi:</b><br> <img class="img-responsive img-border img-full" src="img/cip.jpg" alt=""><br>Cipla has launched its own <b><i>Remedesivir</i></b> under the name of Cipremi. Cipla said it will be commercialising remdesivir through its own facilities and partnered sites. The drug will be supplied through government and open market channels, to ensure equitable distribution. The drug is most effective on those who need oxygen support. Cipla is yet to disclose the pricing for the drug. </p><hr>
  
